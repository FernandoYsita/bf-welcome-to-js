'use strict';

console.log('-- accessing characters in a string by index --');

// examples
console.log('eat'.length); // 3

console.log('eat'[0]); // 'e'
console.log('eat'[1]); // 'a'
console.log('eat'[2]); // 't'

console.log('eat'[0] === 'e'); // true
console.log('eat'[1] === 'a'); // true
console.log('eat'[2] === 't'); // true

// exercises
console.log('JavaScript'.length === 10); // true
console.log('JavaScript'[9]); // 't'
console.log('JavaScript'[0]); // 'J'
console.log('JavaScript'[3] === 'a'); // true
console.log('JavaScript'[4] === 'S'); // true

console.log('programming'.length === 11); // true
